    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
        String name, SensorType, crop, description;
        double minValue, maxValue;
        int quantity;

        if (Name.getText().isEmpty() || crops.getText().isEmpty() || min.getText().isEmpty()
                || max.getText().isEmpty() || qty.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please provide all the necessary details...", "Error", JOptionPane.ERROR_MESSAGE);

        } else {
            name = Name.getText();
            SensorType = comboSensorType.getSelectedItem().toString();
            crop = crops.getText();
            minValue = Double.parseDouble(min.getText());
            maxValue = Double.parseDouble(max.getText());
            quantity = Integer.parseInt(qty.getText());
            description = desc.getText();

            String status = addFertilizer(name, SensorType, crop, description, minValue, maxValue, quantity);
            if (status.equals("success")) {
                JOptionPane.showMessageDialog(this, "You have added the new fertilizer successfully", "Congradulations", JOptionPane.INFORMATION_MESSAGE);
                clear();

                TableLoad(jTable1, theFertilizer);

            } else {
                JOptionPane.showMessageDialog(this, "Adding fertilizer unsuccessful...", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_addMouseClicked

    /**
     *
     * @param Name
     * @param SensorType
     * @param crops
     * @param description
     * @param minValue
     * @param maxValue
     * @param quantity
     * @return
     */
    public String addFertilizer(String Name, String SensorType, String crops, String description, double minValue, double maxValue, int quantity) {
        String status = "";

        new Fertilizer.SingletonBuilder(Name, SensorType, crops, quantity, minValue, maxValue).description(description).build();

        theFertilizer.addFertilizer(Fertilizer.getInstance());
        try {
            Serialization.Serialize(theFertilizer, FILE_NAME_Fertilizer);
            status = "success";
        } catch (Exception ex) {
            status = "fail";
            System.out.println(ex.getMessage());
        }

        return status;
    }

