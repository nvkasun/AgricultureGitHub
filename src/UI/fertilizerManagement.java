    public void clear() {
        ID.setText("");
        comboSensorType.setSelectedItem("Soil Moisture Sensor");
        Name.setText("");
        crops.setText("");
        qty.setText("");
        min.setText("");
        max.setText("");
        desc.setText("");
        search1.setText("");

        theFertilizer.removeAll(theFertilizer);
        try {
            for (Fertilizer fertilizer : Serialization.deserializeFertilizer()) {
                theFertilizer.addFertilizer(fertilizer);
                fertilizer.print();
            }
        } catch (IOException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }

