    /**
     *
     * @param table
     * @param searchList
     */
    public void TableLoad(JTable table, SetOfFertilizer searchList) {
        String[] colName = {"ID", "Name", "Sensor Type", "Crops", "Min", "Max", "Qty.", "Description"};
        Object[][] object = new Object[searchList.size()][8];
        int i = 0;
        if (searchList.size() != 0) {
            for (Fertilizer fertilizer : searchList) {
                object[i][0] = fertilizer.getFertilizerNumber();
                object[i][1] = fertilizer.getName();
                object[i][2] = fertilizer.getSensorType();
                object[i][3] = fertilizer.getCrops();
                object[i][4] = fertilizer.getMinValue();
                object[i][5] = fertilizer.getMaxValue();
                object[i][6] = fertilizer.getQuntity();
                object[i][7] = fertilizer.getDescription();

                i++;

            }
        }

        DefaultTableModel model = new DefaultTableModel(object, colName) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;//This causes all cells to be not editable
            }
        };
        table.setModel(model);
        table.setRowSorter(new TableRowSorter<TableModel>(model));
        table.setAutoscrolls(true);
        table.getTableHeader().setReorderingAllowed(false);
    }

