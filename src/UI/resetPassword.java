    private void btnancSave1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnancSave1MouseClicked
        if (newPWD.getText().equals(confirmPWD.getText())) {
            String status = updatePassword(email, newPWD.getText());
            if (status.equals("success")) {
                new Login().setVisible(true);
                this.hide();
            } else {
                JOptionPane.showMessageDialog(this, "Error updating the password...Try again later...", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "New Password and Confirm Password must be same...", "Error", JOptionPane.ERROR_MESSAGE);
            newPWD.setText("");
            confirmPWD.setText("");
        }

    }//GEN-LAST:event_btnancSave1MouseClicked

    /**
     *
     * @param email
     * @param password
     * @return
     */
    public String updatePassword(String email, String password) {
        String status = "";
        for (User member : searchUsers) {
            if (member.getUsername().equals(email)) {
                member.setPassword(password);
                try {
                    Serialization.Serialize(searchUsers, FILE_NAME_Users);
                    status = "success";
                } catch (IOException ex) {
                    Logger.getLogger(userManagement.class.getName()).log(Level.SEVERE, null, ex);
                    status = "error";
                }
                break;
            }
        }

        return status;
    }

