    /**
     *
     * load the list of product in the users cart
     *
     * @param crops
     */
    public void ListLoad(SetOfCart carts) {

        DefaultListModel listModel = new DefaultListModel();

        int i = 0;
        String data = "";
        listModel.addElement("Product ID    Price   Quantity    Name");
        if (carts.size() != 0) {
            for (SalesCart cart : carts) {
                data = cart.getProductID() + "                 " + cart.getPrice() + "          " + cart.getQuantity() + "        " + cart.getName();
                i++;
                listModel.addElement(data);
            }
        }

        lstmcProductList.setModel(listModel);
        lstmcProductList.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }

