    private void removeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeMouseClicked
        int productID;

        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this product?", "Confirm", JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (confirm == 0) {
            if (productIDtxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please select a product to remove from cart...", "Error", JOptionPane.ERROR_MESSAGE);

            } else {
                productID = Integer.parseInt(productIDtxt.getText());

                for (SalesCart cart : cartSet) {
                    if (cart.getProductID() == productID) {

                        if (cartSet.removeCart(cart)) {

                            try {
                                Serialization.Serialize(cartSet, FILE_NAME_Cart);
                                JOptionPane.showMessageDialog(this, "Product removed from the cart Sucsessfully", "Message", JOptionPane.PLAIN_MESSAGE);
                                ProductsImp product = productSet.getProductFromID(productID).elementAt(0);
                                cartSet.updateProductqty(product, qty);

                                clear();
                              
                                ListLoad(cartSet);
                            } catch (IOException ex) {
                                Logger.getLogger(MyCart.class.getName()).log(Level.SEVERE, null, ex);
                            }

                        } else {
                            JOptionPane.showMessageDialog(this, "Delete unsuccessful...", "Error", JOptionPane.ERROR_MESSAGE);
                        }

                        break;
                    }
                }
            }
        } else {
            clear();
            ListLoad(cartSet);
        }
    }//GEN-LAST:event_removeMouseClicked

