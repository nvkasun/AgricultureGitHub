    /**
     *
     * @param email
     * @param code
     * @return
     */
    public static String validateCode(String email, String code) {
        String status = "";
        for (randomCode random : searchCode) {

            if (random.getEmail().equals(email) && random.getRandomCode().equals(code)) {
                status = "success";
                searchCode.removeCode(random);
                try {
                    Serialization.Serialize(searchCode, FILE_NAME_RandomCodes);
                } catch (IOException ex) {
                    Logger.getLogger(verifyCode.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
            } else {
                status = "error";

            }
        }

        return status;
    }

