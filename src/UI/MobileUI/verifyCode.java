    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(txtVerificationCode1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please provide the verification code...", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
        String status = validateCode(emailReset, txtVerificationCode1.getText());
        if (status == "success") {
            setNewPassword r = new setNewPassword(emailReset);
            r.setVisible(true);
            searchCode.removeAll(searchCode);
            txtVerificationCode1.setText("");
            this.hide();

        } else {
            JOptionPane.showMessageDialog(this, "Varification code not match...", "Error", JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_jButton1MouseClicked

