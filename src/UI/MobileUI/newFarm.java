    private void addfarmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addfarmMouseClicked
        String farmtype, farmername, fieldid, location, longitude, latitude;
        if (cmb_farmtype.getSelectedItem().toString().equals("Select") || txt_farmername.getText().isEmpty() || txt_location.getText().isEmpty()
                || txt_longitude.getText().isEmpty() || txt_latitude.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please provide all the details...", "Error", JOptionPane.ERROR_MESSAGE);

        } else {
            farmtype = cmb_farmtype.getSelectedItem().toString();
            farmername = txt_farmername.getText();
            location = txt_location.getText();
            longitude = txt_longitude.getText();
            latitude = txt_latitude.getText();
            Icon icon = imageIcon.getIcon();

            BufferedImage image = new BufferedImage(icon.getIconWidth(),
                    icon.getIconHeight(), BufferedImage.TYPE_INT_ARGB);
            Graphics2D g2d = image.createGraphics();
            icon.paintIcon(null, g2d, 0, 0);
            g2d.dispose();

            try (ByteArrayOutputStream baos = new ByteArrayOutputStream()) {
                ImageOutputStream ios = ImageIO.createImageOutputStream(baos);
                try {
                    ImageIO.write(image, "png", ios);
                    // Set a flag to indicate that the write was successful
                } finally {
                    ios.close();
                }
                byte[] imageInByte = baos.toByteArray();

                String status = addfarm(farmtype, farmername, location, longitude, latitude, imageInByte);
                if (status.equals("success")) {
                    try {
                        Serialization.Serialize(thefarms, FILE_NAME_farms);
                    } catch (IOException ex) {
                        Logger.getLogger(userManagement.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    JOptionPane.showMessageDialog(this, "You have added the farm successful", "Congradulations", JOptionPane.INFORMATION_MESSAGE);
                    reset();
                    
                } else {
                    JOptionPane.showMessageDialog(this, "Registration unsuccessful...", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (IOException ex) {
                Logger.getLogger(manage_farm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_addfarmMouseClicked

