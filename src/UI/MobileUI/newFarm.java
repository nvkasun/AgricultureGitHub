    public void get_image() {
        chooser = new JFileChooser();
        filter = new FileNameExtensionFilter("JPG & GIF Images", "jpg", "gif", "png");
        chooser.setFileFilter(filter);
        int result = chooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            ImageIcon icon = new ImageIcon(chooser.getSelectedFile().getAbsolutePath());
            Image img = icon.getImage();
            Image newimg = img.getScaledInstance(imageIcon.getWidth(), imageIcon.getHeight(), java.awt.Image.SCALE_SMOOTH);
            ImageIcon imi = new ImageIcon(newimg);
            imageIcon.setIcon(imi);
        }
    }
    
    //reister a new farm
    public String addfarm(String farmtype, String farmername, String location, String longitude, String latitude, byte[] image) {
        String status = "";
        new farms.SingletonBuilder(farmtype, farmername, location, longitude, latitude).image(image).build();
        thefarms.addfarm(farms.getInstance());
        try {
            Serialization.Serialize(thefarms, FILE_NAME_farms);
            status = "success";
        } catch (Exception ex) {
            status = "fail";
            System.out.println(ex.getMessage());
        }

        return status;
    }


