    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        int fertilizerNum;
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this fertilizer?", "Confirm", JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if (confirm == 0) {
            if (fertilizerID.equals(" ") || fertilizerID.equals("Fertilizer")) {
                JOptionPane.showMessageDialog(this, "Please select a fertilizer to delete...", "Error", JOptionPane.ERROR_MESSAGE);

            } else {
                fertilizerNum = Integer.parseInt(fertilizerID);

                Fertilizer fertilizer = theFertilizer.getFertilizerFromNumber(Integer.parseInt(fertilizerID)).firstElement();
                boolean status = theFertilizer.removeFertilizer(fertilizer);
                if (status) {
                    try {
                        Serialization.Serialize(theFertilizer, FILE_NAME_Fertilizer);
                    } catch (IOException ex) {
                        Logger.getLogger(userList.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    JOptionPane.showMessageDialog(this, "You have deleted successful", "Congradulations", JOptionPane.INFORMATION_MESSAGE);
                    fertilizerID = "";
                    theFertilizer.removeAll(theFertilizer);
                    try {
                        for (Fertilizer fertilize : Serialization.deserializeFertilizer()) {
                            theFertilizer.addFertilizer(fertilize);
                            fertilizer.print();
                        }
                    } catch (IOException | ClassNotFoundException ex) {
                        JOptionPane.showMessageDialog(this, ex.getMessage());
                    }

                    ListLoad(listFertilizer, theFertilizer);
                    JOptionPane.showMessageDialog(this, "You have deleted successful", "Congradulations", JOptionPane.INFORMATION_MESSAGE);

                } else {
                    JOptionPane.showMessageDialog(this, "Delete unsuccessful...", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_deleteMouseClicked

