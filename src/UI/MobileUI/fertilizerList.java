    /**
     *
     * @param list
     * @param searchList
     */
    public static void ListLoad(JList list, SetOfFertilizer searchList) {
        DefaultListModel listModel = new DefaultListModel();
        list.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        int i = 0;
        String data = "";
        if (searchList.size() != 0) {
            listModel.addElement("Fertilizer Number Fertilizer Name");
            for (Fertilizer fertilizer : searchList) {
                data = fertilizer.getFertilizerNumber() + "     " + fertilizer.getName();
                i++;

                listModel.addElement(data);
            }

        }

        list.setModel(listModel);
    }

